
;PRINTING ARRAY USING NORMAL VARIABLE 
.MODEL SMALL
.STACK 100H
.DATA

A DB 1,3,4,6

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    MOV CX,4  ;ELEMENT COUNTER
    XOR BX,BX
    
    TOP:
    MOV AH,2
    MOV DL,A+BX ;USING NORMAL ADDRESSING MODE
    ADD DL,48
    INT 21H
    
    MOV AH,2
    MOV DL,' '
    INT 21H
    
    ADD BX,1
    LOOP TOP
    
    MOV AH,4CH
    INT 21H
    
    MAIN ENDP
END MAIN